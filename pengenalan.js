/**
 * Apa itu Synchronous 
 * 
 *  - program js secara default dijalankan secara baris perbaris
 *  - jadi baris selanjutnya bisa dieksekusi jika baris sebelumnya telah dieksekusi
 *  - Synchronous juga bisa disebut {Blocking} karena tiap proses harus menunggu, baru proses
 *    berikutnya dieksekusi 
 * 
 * Apa itu Asynchronous
 *  - pada proses asynchronous, JS tidak akan menunggu suatu proses selesai, melainkan JS akan
 *    melanjutkan baris selanjutnya, tanpa harus menunggu proses asynchronous selesai
 *  - Asynchronous juga disebut non Blocking 
 */